const { readFile, writeFile } = require('fs')
console.log('task started...')
readFile('./content/first.txt', { encoding: 'utf-8' }, (err, res) => {
  if (err) {
    console.log(err)
    return
  }
  const first = res
  readFile('./content/sub/second.txt', { encoding: 'utf-8' }, (err, res) => {
    if (err) {
      console.log(err)
      return
    }
    const second = res
    writeFile(
      './final/final.txt',
      `First text content: ${first}, second text content: ${second}`,
      (err, res) => {
        if (err) {
          console.log(err)
          return
        }
        console.log('File created succesfully!')
      }
    )
  })
})
